import { spawnSync } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { renderFile } from "../experiments/layouter/src/pipeline.mjs";

const repo = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const generated = path.join(repo, "docs", "generated");

const documentationDiagrams = [
  ["getting-started-first", "getting-started-first", {}],
  ["getting-started-reusable", "getting-started-reusable", {}],
  ["getting-started-layout", "getting-started-layout", {}],
  ["getting-started-route-auto", "getting-started-route-auto", {}],
  ["getting-started-route-segment", "getting-started-route-segment", {}],
  ["getting-started-private-dock", "getting-started-private-dock", {}],
  ["getting-started-styling", "getting-started-styling", {}],
  ["getting-started-orientation-depth", "getting-started-orientation-depth", {}],
  ["routing-corridors", "routing-corridors", {}],
  ["routing-corridors", "routing-corridors-debug", { debugRouting: true }],
  ["port-sharing", "port-sharing", {}],
  ["port-sharing", "port-sharing-debug", { debugRouting: true }],
  ["port-groups", "port-groups", {}],
  ["port-groups", "port-groups-debug", { debugRouting: true }],
  ["adaptive-service", "adaptive-service-wide", { inlineSize: 240, blockSize: 180 }],
  ["adaptive-service", "adaptive-service-narrow", { inlineSize: 60, blockSize: 80 }],
  ["orientation", "orientation", {}],
  ["render-pipeline", "render-pipeline", {}],
];

function runLocalScript(script, args = []) {
  const result = spawnSync(process.execPath, [path.join(repo, script), ...args], {
    cwd: repo,
    stdio: "inherit",
  });
  if (result.error) throw result.error;
  if (result.status !== 0) throw new Error(`${script} exited with status ${result.status}`);
}

await mkdir(generated, { recursive: true });
for (const [sourceName, outputName, options] of documentationDiagrams) {
  const source = path.join(repo, "docs", "diagrams", `${sourceName}.tsx`);
  const output = path.join(generated, `${outputName}.svg`);
  const { scene } = await renderFile(source, output, options);
  console.log(`docs/${outputName}: ${scene.width}x${scene.height}`);
}

const documentationGallery = documentationDiagrams.map(([sourceName, outputName]) => `
  <tr>
    <td><a href="../diagrams/${sourceName}.tsx"><code>${sourceName}.tsx</code></a></td>
    <td><img src="./${outputName}.svg" alt="Generated ${outputName} diagram" width="100%"></td>
  </tr>`).join("");

await writeFile(path.join(generated, "README.md"), `# Generated documentation diagrams

> Generated by \`npm run build\` from the TSX sources in [\`docs/diagrams/\`](../diagrams/). Do not edit these SVGs by hand.

The normal and routing-debug variants use the same local evaluator, layout engine, router, and SVG painter as the repository examples.

<table>
  <tr><th>Kvísl source</th><th>Generated SVG</th></tr>${documentationGallery}
</table>
`, "utf8");

runLocalScript("experiments/layouter/render-all.mjs");
runLocalScript("experiments/layouter/render-all.mjs", ["--debug-routing"]);
await renderFile(
  path.join(repo, "examples", "modelplane-fleet-inference", "neon-infrastructure.tsx"),
  path.join(repo, "experiments", "layouter", "output", "modelplane-fleet-inference-neon.svg"),
);

console.log("All documentation, README, and example diagrams were rebuilt with the local Kvísl prototype.");
